import androidx.compose.ui.tooling.preview.Preview
import androidx.compose.ui.unit.dp
import com.example.superheroes.model.HeroesRepository
import com.example.superheroes.ui.theme.SuperheroesTheme

class MainActivity : ComponentActivity() {
    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
        enableEdgeToEdge()
        setContent {
            SuperheroesTheme {
                Surface(
                    modifier = Modifier.fillMaxSize(),
                    color = MaterialTheme.colorScheme.background
                ) {
                    SuperheroesApp()
                }
            }
        }
    }

    @Composable
    fun SuperheroesApp() {
        var isReversed by remember { mutableStateOf(false) }

        Scaffold(
            modifier = Modifier.fillMaxSize(),
            topBar = { TopAppBar() }
        ) { paddingValues ->

            val heroes = HeroesRepository.heroes
            val displayHeroes = if (isReversed) heroes.reversed() else heroes

            // Usamos un Column para poner el botón arriba y la lista abajo
            Column(modifier = Modifier.padding(paddingValues)) {
                Button(
                    onClick = { isReversed = !isReversed },
                    modifier = Modifier.fillMaxWidth().padding(16.dp)
                ) {
                    Text("Invertir Lista")
                }

                HeroesList(heroes = displayHeroes, contentPadding = androidx.compose.foundation.layout.PaddingValues(0.dp))
            }
        }
    }

    @OptIn(ExperimentalMaterial3Api::class)
    @Composable
    fun TopAppBar(modifier: Modifier = Modifier) {
        CenterAlignedTopAppBar(
            title = {
                Text(
                    text = stringResource(R.string.app_name),
                    style = MaterialTheme.typography.displayLarge,
                )
            },
            modifier = modifier
        )
    }

    @Preview(showBackground = true)
    @Composable
    fun SuperHeroesPreview() {
        SuperheroesTheme {
            SuperheroesApp()
        }
    }
}